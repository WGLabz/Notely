/**
 * sync-design-tokens.cjs
 *
 * Extracts CSS custom property blocks from src/styles.css and writes them to
 * docs-site/.vitepress/theme/tokens.css so the VitePress docs always inherit
 * the application's design tokens without manual duplication.
 *
 * Usage:
 *   node scripts/sync-design-tokens.cjs
 *
 * Called automatically by `npm run docs:build`.
 */

"use strict";

const fs = require("node:fs");
const path = require("node:path");

const ROOT = path.resolve(__dirname, "..");
const SRC_CSS = path.join(ROOT, "src", "styles", "variables.css");
const OUT_CSS = path.join(ROOT, "docs-site", ".vitepress", "theme", "tokens.css");

function extractCssBlocks(css) {
  const blocks = [];

  // Match :root { ... } and :root[data-theme="dark"] { ... }
  // Also captures @supports blocks that only contain root variable overrides
  const selectors = [
    /:root\s*\{/g,
    /:root\[data-theme="dark"\]\s*\{/g,
    /@supports\s*\([^)]+\)\s*\{\s*:root\s*\{/g,
  ];

  for (const pattern of selectors) {
    let match;
    const re = new RegExp(pattern.source, "g");
    while ((match = re.exec(css)) !== null) {
      const start = match.index;
      let depth = 0;
      let i = start;
      while (i < css.length) {
        if (css[i] === "{") depth++;
        else if (css[i] === "}") {
          depth--;
          if (depth === 0) {
            blocks.push(css.slice(start, i + 1).trim());
            break;
          }
        }
        i++;
      }
    }
  }

  return blocks;
}

function run() {
  if (!fs.existsSync(SRC_CSS)) {
    console.error(`[sync-design-tokens] Source not found: ${SRC_CSS}`);
    process.exit(1);
  }

  const srcCss = fs.readFileSync(SRC_CSS, "utf8");
  const blocks = extractCssBlocks(srcCss);

  if (blocks.length === 0) {
    console.error("[sync-design-tokens] No CSS variable blocks found in styles.css");
    process.exit(1);
  }

  const outDir = path.dirname(OUT_CSS);
  fs.mkdirSync(outDir, { recursive: true });

  const banner = [
    "/*",
    " * tokens.css — AUTO-GENERATED by scripts/sync-design-tokens.cjs",
    " * Source: src/styles.css",
    ` * Generated: ${new Date().toISOString()}`,
    " *",
    " * DO NOT EDIT MANUALLY. Run: node scripts/sync-design-tokens.cjs",
    " */",
    "",
  ].join("\n");

  // Filter out non-variable lines from :root blocks (e.g. color:, background:, font-family:)
  // We only want --custom-property definitions
  const filtered = blocks
    .map((block) => {
      const lines = block.split("\n");
      return lines
        .filter((line) => {
          const trimmed = line.trim();
          // Keep selector lines, closing braces, and CSS custom properties
          if (trimmed.startsWith(":root") || trimmed.startsWith("@supports") || trimmed === "{" || trimmed === "}" || trimmed === "") return true;
          if (trimmed.startsWith("--")) return true;
          // Keep closing braces with depth
          if (trimmed === "}" || trimmed === "};") return true;
          return false;
        })
        .join("\n");
    })
    .join("\n\n");

  fs.writeFileSync(OUT_CSS, banner + filtered + "\n");
  console.log(`[sync-design-tokens] Written ${blocks.length} block(s) to ${OUT_CSS}`);
}

run();
